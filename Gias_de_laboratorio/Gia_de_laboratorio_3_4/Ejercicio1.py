texto = "python"

# Encontrar la longitud del texto
longitud = len(texto)

# Convertir la longitud a float
longitud_float = float(longitud)

# Convertir la longitud float a string
longitud_string = str(longitud_float)

print("Longitud original del texto:", longitud)
print("Longitud como float:", longitud_float)
print("Longitud como string:", longitud_string)